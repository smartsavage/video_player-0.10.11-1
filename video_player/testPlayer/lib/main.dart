/// An example of using the plugin, controlling lifecycle and playback of the
/// video.

import 'package:flutter/cupertino.dart';
import 'package:flutter/material.dart';
import 'package:video_player/video_player.dart';

void main() {
  runApp(
    MaterialApp(
      home: _App(),
    ),
  );
}

class _App extends StatelessWidget {
  @override
  Widget build(BuildContext context) {
    return DefaultTabController(
      length: 2,
        child: Scaffold(
        key: const ValueKey<String>('home_page'),
        appBar: AppBar(
          title: const Text('Video player example'),
          actions: <Widget>[
            IconButton(
              key: const ValueKey<String>('push_tab'),
              icon: const Icon(Icons.navigation),
              onPressed: () {
                Navigator.push<_PlayerVideoAndPopPage>(
                  context,
                  MaterialPageRoute<_PlayerVideoAndPopPage>(
                    builder: (BuildContext context) => _PlayerVideoAndPopPage(),
                  ),
                );
              },
            )
          ],
          bottom: const TabBar(
            isScrollable: true,
            tabs: <Widget>[
              Tab(
                text: "Live",
              ),
              Tab(text: "Archive"),
            ],
          ),
        ),
        body: TabBarView(
          children: <Widget>[
            _LiveVideo(),
            _ArchiveVideo(),
          ],
        ),
      ),
    );
  }
}



class _ArchiveVideo extends StatefulWidget {
  @override
  _ArchiveVideoState createState() => _ArchiveVideoState();
}

class _ArchiveVideoState extends State<_ArchiveVideo> {
  VideoPlayerController _controller;
  bool ccOn = false;
  @override
  void initState() {
    super.initState();
    _controller = VideoPlayerController.network(
      'https://parlvuvod.azureedge.net/pvvodhoc-en/_definst_/mp4:azrhoc/archives/PV750/2020/2020-06-23/33507_AGRI%20Meeting%20No.%2020_10-59-33_VL.mp4/playlist.m3u8'
    );
    _controller.addListener(() {
      setState(() {});
    });
    _controller.setLooping(true);
    _controller.initialize().then((_) => setState(() {}));
    _controller.play();
  }

  @override
  void dispose() {
    _controller.dispose();
    super.dispose();
  }

  @override
  Widget build(BuildContext context) {
    return SingleChildScrollView(
      child: Column(
        children: <Widget>[
          Container(padding: const EdgeInsets.only(top: 20.0)),
          Container(
            padding: const EdgeInsets.all(20),
            child: AspectRatio(
              aspectRatio: _controller.value.aspectRatio,
              child: Stack(
                alignment: Alignment.bottomCenter,
                children: <Widget>[
                  VideoPlayer(_controller),
                  ClosedCaption(text: _controller.value.subtitle),
                  _PlayPauseOverlay(controller: _controller),
                  VideoProgressIndicator(_controller, allowScrubbing: true),
                ],
              ),
            ),
          ),
          ClosedCaption(text: _controller.value.metadata),
          FlatButton(
            color: Colors.blue,
            textColor: Colors.white,
            disabledColor: Colors.grey,
            disabledTextColor: Colors.black,
            padding: EdgeInsets.all(8.0),
            splashColor: Colors.blueAccent,
            onPressed: () {
              if (ccOn){
                ccOn =false;
                _controller.ccOff();
              }
              else{
                ccOn =true;
                _controller.ccOn();
              }
            },
            child: Text(
              "CC",
              style: TextStyle(fontSize: 20.0),
            ),
          ),
        ],
      ),
    );
  }
}

